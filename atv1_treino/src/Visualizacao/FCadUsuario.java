    private void butCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butCadActionPerformed
        user.setNome(txtNome.getText());
        user.setEmail(txtEmail.getText());
        user.setLogin(txtLogin.getText());
        user.setSenha(txtSenha.getText());
        
        user.cadastrarUsuario();
        ResultSet tabela = user.consultar();
        DefaultTableModel modelo = (DefaultTableModel) txtTabela.getModel();
        modelo.setNumRows(0);
        try {
            while (tabela.next()) {
                modelo.addRow(new String[]{
                    tabela.getString(1),
                    tabela.getString(2),
                    tabela.getString(3),
                    tabela.getString(4)
                });
            }
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher tabela: " + erro);
        }
                
    }//GEN-LAST:event_butCadActionPerformed

